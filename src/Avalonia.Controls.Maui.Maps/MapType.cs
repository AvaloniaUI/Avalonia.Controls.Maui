namespace Avalonia.Controls.Maui.Maps;

/// <summary>
/// Enumerates the display style of the map.
/// </summary>
public enum MapType
{
    /// <summary>
    /// Displays a street map.
    /// </summary>
    Street,

    /// <summary>
    /// Displays a satellite map.
    /// </summary>
    Satellite,

    /// <summary>
    /// Displays a satellite map with street data superimposed.
    /// </summary>
    Hybrid
}
