namespace Avalonia.Controls.Maui;

/// <summary>
/// Indicates pan gesture status
/// </summary>
public enum PanGestureStatus
{
    /// <summary>
    /// The pan gesture has started.
    /// </summary>
    Started,

    /// <summary>
    /// The pan gesture is currently in progress.
    /// </summary>
    Running,

    /// <summary>
    /// The pan gesture has completed.
    /// </summary>
    Completed
}
